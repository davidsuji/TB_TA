    {
        //
        $Dosen = $this->validate(request(),
        [
          'NIP'=>'required',
          'Nama_Dosen'=>'required|string|max:40',
          'Jurusan'=>'required',
          'Bidang_Keahlian'=>'required' ,
          'No_Hp'=>'required' ,

        ]);

        Dosen::create($Dosen);
        return back()->with('success', 'Data Dosen has been added');
